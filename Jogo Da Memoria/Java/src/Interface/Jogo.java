    private String[] initComboBox()
    {
        int num = new ManipularPastas().lerTodasImagensNaPasta().size();
        List<String> modelosDisponiveis = new ArrayList<>();
        int matriz[][] = {{2, 3}, {2, 4}, {2, 5}, {3, 4}, {3, 6}, {4, 4}, {4, 6}, {4, 7}};
        for(int i = 0; i < matriz.length; i++)
        {
            if((matriz[i][0] * matriz[i][1])/2 <= num)
            {
                modelosDisponiveis.add(matriz[i][0]+" x "+matriz[i][1]);
            }
        }
        String array[] = new String[modelosDisponiveis.size()];
        array = modelosDisponiveis.toArray(array);
        jComboBox1.setModel(new DefaultComboBoxModel(array));
        return array;
    }
