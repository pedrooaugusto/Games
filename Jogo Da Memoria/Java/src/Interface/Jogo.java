    public class Cronometro extends Thread
    {
        private boolean stop = false;
        private int sec = 0, min = 0, hour = 0;
        private String tempo = "";
        
        public void setStop(boolean stop)
        {
            this.stop = stop;
        }
        public boolean getStop()
        {
            return this.stop;
        }
        private String formatarTempo(int valor)//Auxuliar da de baixo
        {
            return String.valueOf((valor <= 9) ? "0"+valor : ""+valor);
        }
        public void reiniciar()
        {
            this.sec = 0;
            this.min = 0;
            this.hour = 0;
            this.tempo = "";
            this.stop = false;
        }
        @Override
        public void run()
        {
            while(true)
            {
                if(stop == false && quadro1.restam() != 0)
                {
                    sec++;
                    if (sec == 60)
                    {
                        sec = 0;
                        min++;
                    }
                    if (min == 60)
                    {
                        min = 0;
                        hour++;
                    }
                    tempo = formatarTempo(hour) + ":" + formatarTempo(min) + ":" + formatarTempo(sec);
                    jLabel5.setText(tempo);
                }
                try
                {
                    sleep(1000);
                } catch (Exception ex)
                {
                    ex.printStackTrace();
                }
            }
        }
    }
    
